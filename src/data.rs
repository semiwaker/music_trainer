pub fn interval_name(mut x: usize) -> &'static str {
  while x > 12 {
    x -= 12;
  }
  match x {
    0 => "P1",
    1 => "m2",
    2 => "M2",
    3 => "m3",
    4 => "M3",
    5 => "P4",
    6 => "A4",
    7 => "P5",
    8 => "m6",
    9 => "M6",
    10 => "m7",
    11 => "M7",
    12 => "P8",
    _ => panic!(),
  }
}
